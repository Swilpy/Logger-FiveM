-- Fonction pour obtenir le Discord du joueur
function GetPlayerDiscord(playerSource)
    local identifiers = GetPlayerIdentifiers(playerSource)

    for _, identifier in ipairs(identifiers) do
        if string.match(identifier, "discord:") then
            local discordPing = string.gsub(identifier, "discord:", "")
            return "<@" .. discordPing .. ">" -- Formater pour mentionner le pseudo Discord
        end
    end

    return "Non disponible" -- Le joueur n'a pas de Discord enregistré
end


AddEventHandler('playerConnecting', function()
    local player = source
    local name = GetPlayerName(player)
    local ip = GetPlayerEndpoint(player)
    local ping = GetPlayerPing(player)
    local steamhex = GetPlayerIdentifier(player)
    local discord = GetPlayerDiscord(player)
    local currentTime = os.date("%H:%M:%S") -- Heure actuelle au format HH:MM:SS

    local connect = {
        {
            ["color"] = 3066993, -- Couleur verte pour indication de connexion
            ["author"] = {
                ["name"] = name,
                ["icon_url"] = "https://cdn.discordapp.com/avatars/" .. discord:gsub("[<>@!]", "") .. "/avatar.png" -- Image du profil Discord du joueur
            },
            ["title"] = "Connexion de " .. name,
            ["description"] = "Un joueur vient de se connecter au serveur.",
            ["fields"] = {
                {
                    ["name"] = "Informations",
                    ["value"] = "**__IP__**: " .. ip .. "\n**__Steam ID__**: " .. steamhex .. "\n**__Discord__**: " .. discord .. "\n**__Ping__**: " .. ping .. "ms\n**__Heure__**: " .. currentTime,
                    ["inline"] = false
                }
            },
            ["footer"] = {
                ["text"] = communityname,
                ["icon_url"] = communitylogo,
            },
        }
    }

    PerformHttpRequest(logs, function(err, text, headers) end, 'POST', json.encode({embeds = connect}), { ['Content-Type'] = 'application/json' })
end)

